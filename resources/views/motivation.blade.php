<h2>Career anchors</h2>

For me personally, the 'work-life balance' is probably the most important thing. I think it's very important to have a life besides work. This
includes things like sports, hobbies and spending time with friends and family. A good work-life balance also allows you to take better care of
yourself. You could, for example, spend more time preparing healthy food and make sure you stay healthy mentally and physically.
<br>
<br>
The second most important for me, is the 'doing your very best' anchor. If you try your best, other people are inevitably going to notice. This
means that they will often be more than willing  to help you out if you're struggling with something. That is because they see that you care and
take your job seriously. Your boss will also appreciate a hard working employee. This could also mean that you're more likely to earn yourself a
raise. From my own experience, I have noticed, people don't really mind if you make mistakes, as long as you are genuinly trying your best.
<br>
<br>
The least important anchor to me, is the anchor about status. Generally speaking I'm not too worried about what most people think about me. Therefore
I don't really care if they think highly of my future job or not.



