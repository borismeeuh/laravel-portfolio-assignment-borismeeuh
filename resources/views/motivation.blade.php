<h2>Theory</h2>

In order to pass this HBO ICT course, you need motivation. You need something that drives you to keep up the work, hand in assignments on time etc. In this
paragraph, i'll explain what drives me to succesfully complete this course and discuss several different types of motivation.
<br>
First of all, what does motivation mean? The Cambridge Dictionary defines it as follows: Enthusiasm for doing something. That is in my opinion
a clear, straight forward way the explain the meaning of 'motivation'. Motivation is the reason or motive as to why you are doing something.
<br>
There are several important types of motivation that make me want to complete this course. Firstly, there is competence motivation. I want to keep getting
better at coding for example. In addition, I want to prove to myself that I able to succesfully complete an HBO course. Secondly, I want to be able to
sustain myself and my possible family later on in life. This means, that I'll have to get a good job to earn a good living. In order to obtain such a job, i'll
have to get a diploma. Thirdly, there is the financial motivation. There are opportunities in the IT field, that there may as well be infinite. There are plenty
of job opportunities and each job has a plethora of ways to climb the social ladder and make more money.
<br>
<br>
Most people divide motivation in two main types:

<ul style="list-style: circle;">
    <li>Intrinsic motivation</li>
    <li>Extrinsic motivation</li>
</ul>

<br>
These two main types of motivation more or less speak for them selves. Intrinsic motivation is anything that motivates you as an individual. It is
therefore based on internal award. That means that the results of your achievements, reached through intrinsic motivation, are not always visible to other people.
Examples include getting over internal fears and internal desires. Extrinsic motivation, like the name implies, are the things that motivate you based on external
rewards like money or praise.
<br>
There are other sub-categories of motivation too. These include, but are not limited to:

<ul style="list-style: circle;">
    <li>Achievement motivation - Here, the achievement is the primairy goal, not the reward</li>
    <li>Competence motivation - This type of motivation could mean someone wants to develop a certain skill really well.</li>
    <li>Power motivation - Here, people are motivated by the will to influence other people.</li>
</ul>

<br>
<h2>TED talk</h2>

According to Dan Pink, motivation revolves around three things: autonomy, mastery and purpose. Autonomy  means the urge to direct your own life.
Mastery means the desire to get better and better at something. Purpose means the yearning to what we do in the service of something larger than ourselves.
<br>
<br>
According to Dan Pink, autonomy motivates people way more than money. Accoriding to Dan Pink, people who have autonomy
over their time, focus and ideas are more productive than people who are forced to work at something by their supervisors. When they are not
told to work at something, they can work on whatever they want in both a creative and productive manner.


